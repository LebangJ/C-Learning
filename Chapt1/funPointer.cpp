#include "iostream"
using namespace std;
//函数指针是一种「特殊的指针变量」，它存储的不是普通变量的内存地址，而是「函数在内存中的入口地址」。

//    普通指针：指向数据，解引用操作 *p 访问的是「数据值」；
//    函数指针：指向函数，调用操作 (*fp)() / fp() 执行的是「函数体代码」。
/*定义
返回值类型 (*函数指针变量名)(参数类型1, 参数类型2, ...);
简化理解：把函数声明的函数名，替换为 (*指针名) 即可
*/
// 目标函数1：无参、返回int
int func1() { return 10; }
// 目标函数2：2个int参数、返回int
int func2(int a, int b) { return a + b; }
// 目标函数3：无参、无返回值
void func3() { cout << "Hello FuncPtr" << endl; }

int main() {
    // ✅ 定义函数指针，匹配func1（无参、返回int）
    int (*fp1)();
    // ✅ 定义函数指针，匹配func2（2个int参数、返回int）
    int (*fp2)(int, int);
    // ✅ 定义函数指针，匹配func3（无参、无返回值）
    void (*fp3)();
    // 两种赋值方式完全等价，推荐第一种（更简洁）
    fp1 = func1;   // ✅ 推荐：函数名直接赋值（函数名=函数地址）
    fp1 = &func1;  // ✅ 兼容写法：加取地址符，效果一致
    fp2 = func2;   // 绑定func2
    fp3 = func3;   // 绑定func3

     // ========== 方式1：解引用调用（最符合指针语义，推荐） ==========
    int res1 = (*fp1)();          // 等价于 func1() → res1=10
    int res2 = (*fp2)(10, 20);    // 等价于 func2(10,20) → res2=30
    (*fp3)();                     // 等价于 func3() → 打印Hello
     // ========== 方式2：直接调用（编译器语法糖，最简洁，推荐） ==========
    res1 = fp1();
    res2 = fp2(10,20);
    fp3();
    //也需要判断是否空



    
    return 0;
}